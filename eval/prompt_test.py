def build_user(turns, sources) -> str:
    kb = "\n".join(f"[{i+1}] {s['title']}: {s['snippet']}" for i, s in enumerate(sources)) or "(пусто)"
    hist = "\n".join(f"{'Клиент' if t['speaker']=='customer' else 'Оператор'}: {t['text']}"
                     for t in turns)
    return (f"## Документы базы знаний\n{kb}\n\n## Диалог (клиент раздражён)\n{hist}\n\n"
            "Подскажи ОДНУ короткую реплику оператора клиенту — только саму реплику, без пояснений.")


def clean(raw: str) -> str:
    raw = re.sub(r"<think>.*?</think>", "", raw, flags=re.DOTALL)
    raw = raw.replace("<think>", "").replace("</think>", "").strip()
    raw = re.sub(r"^(Подсказка|Реплика|Оператор)\s*:\s*", "", raw, flags=re.I).strip()
    raw = raw.split("\n\n")[0].strip()
    return raw.strip('"«»').strip()


def main() -> None:
    from app.pipeline.llm import get_generator
    gen = get_generator(); gen._ensure_loaded()
    print("Qwen3 загружен", flush=True)
    hard = json.loads((R / "hard_subset.json").read_text(encoding="utf-8"))

    for vname, vprompt in VARIANTS.items():
        sysp = vprompt + "\n/no_think"
        out = []
        for d in hard:
            user = build_user(d["transcript"], d["sources"])
            res = gen._llm.create_chat_completion(
                messages=[{"role": "system", "content": sysp},
                          {"role": "user", "content": user}],
                max_tokens=220, temperature=0.6, top_p=0.8,
                stop=["\n\n\n", "<|im_end|>", "<|message_sep|>"],
            )
            sug = clean(res["choices"][0]["message"]["content"])
            out.append({"dialogue_id": d["dialogue_id"], "turn_idx": d["turn_idx"],
                        "dataset": d["dataset"], "asr_text": d["asr_text"],
                        "ideal_text": d["ideal_text"], "suggestion": sug})
        (R / f"prompt_{vname}.json").write_text(json.dumps(out, ensure_ascii=False, indent=2),
                                                encoding="utf-8")
        print(f"  {vname}: {len(out)} реплик -> prompt_{vname}.json", flush=True)
    print("готово")


if __name__ == "__main__":
    main()
