SPEAKERS = ["aidar", "baya", "kseniya", "xenia", "eugene"]
SR = 24000


def telephone(au: np.ndarray, sr: int, rng: np.random.RandomState) -> np.ndarray:
    """Реализм колл-центра: 8кГц полоса + μ-law кодек + шум + лёгкая реверберация."""
    import librosa
    x = au.astype("float32")
    ir_len = int(0.05 * sr)
    ir = (rng.randn(ir_len).astype("float32") * np.exp(-np.linspace(0, 6, ir_len))) * 0.3
    ir[0] = 1.0
    x = np.convolve(x, ir)[: len(au)]
    x8 = librosa.resample(x, orig_sr=sr, target_sr=8000)
    mu = 255.0
    x8 = np.clip(x8 / (np.abs(x8).max() + 1e-6), -1, 1)
    comp = np.sign(x8) * np.log1p(mu * np.abs(x8)) / np.log1p(mu)
    q = np.round(comp * 127) / 127
    decomp = np.sign(q) * (1 / mu) * ((1 + mu) ** np.abs(q) - 1)
    x = librosa.resample(decomp.astype("float32"), orig_sr=8000, target_sr=sr)
    snr = rng.uniform(15, 25)
    p_sig = np.mean(x ** 2) + 1e-9
    p_noise = p_sig / (10 ** (snr / 10))
    x = x + rng.randn(len(x)).astype("float32") * np.sqrt(p_noise)
    return (x / (np.abs(x).max() + 1e-6) * 0.95).astype("float32")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", type=Path, default=Path(r"K:\.caches\synth"))
    ap.add_argument("--no-aug", action="store_true", help="без телефонной аугментации")
    args = ap.parse_args()
    wav_dir = args.out / "wav"; wav_dir.mkdir(parents=True, exist_ok=True)
    man = args.out / "manifest.jsonl"
    rng = np.random.RandomState(123)

    model, _ = torch.hub.load("snakers4/silero-models", "silero_tts",
                               language="ru", speaker="v4_ru")
    model.to("cpu")

    n = 0
    with man.open("w", encoding="utf-8") as mf:
        for emo, lines in LINES.items():
            for li, text in enumerate(lines):
                for sp in SPEAKERS:
                    try:
                        au = model.apply_tts(text=text, speaker=sp, sample_rate=SR).numpy()
                    except Exception as exc:  # noqa: BLE001
                        print(f"  skip {emo}/{li}/{sp}: {exc}", flush=True)
                        continue
                    if not args.no_aug:
                        au = telephone(au, SR, rng)
                    fid = f"{emo}_{li:02d}_{sp}"
                    sf.write(wav_dir / f"{fid}.wav", au.astype("float32"), SR, subtype="PCM_16")
                    mf.write(json.dumps({"id": fid, "path": str(wav_dir / f'{fid}.wav'),
                                          "emotion": emo, "text": text, "speaker": sp,
                                          "line": li}, ensure_ascii=False) + "\n")
                    n += 1
            print(f"  {emo}: готово", flush=True)
    print(f"синтез: {n} wav -> {wav_dir}  (aug={'off' if args.no_aug else 'telephone+noise'})", flush=True)


if __name__ == "__main__":
    main()
