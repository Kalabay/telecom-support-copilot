"""Генерирует docs/fusion_story.pdf — полный разбор пути с эмоциями простым языком."""

from __future__ import annotations

import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PROJECT_ROOT / "scripts"))

from make_explainer_pdf import (  # noqa: E402
    ACCENT,
    BOX_BG,
    BOX_BORDER,
    INK,
    MUTED,
    Doc,
    bullets,
    callout,
    code,
    h1,
    h2,
    para,
    table,
)

OUT = PROJECT_ROOT / "docs" / "fusion_story.pdf"


def build() -> None:
    doc = Doc()

    doc.add_page()
    doc.ln(34)
    doc.set_font("ui", "B", 22)
    doc.set_text_color(*ACCENT)
    doc.multi_cell(0, 11, "Как мы улучшали распознавание\nэмоций по голосу")
    doc.ln(2)
    doc.set_font("ui", "", 14)
    doc.set_text_color(*INK)
    doc.multi_cell(0, 7, "Весь путь простыми словами: от готовой модели\n"
                          "до собственного обучаемого метода слияния модальностей")
    doc.ln(8)
    doc.set_font("ui", "", 11)
    doc.set_text_color(*MUTED)
    doc.multi_cell(0, 6,
        "LLM-копилот для телеком-техподдержки - исследовательская часть (ML) - НИУ ВШЭ - 2026")
    doc.ln(10)
    doc.set_draw_color(*BOX_BORDER)
    doc.set_fill_color(*BOX_BG)
    y = doc.get_y()
    doc.rect(doc.l_margin, y, doc.w - doc.l_margin - doc.r_margin, 40, style="DF")
    doc.set_xy(doc.l_margin + 5, y + 4)
    doc.set_font("ui", "B", 11)
    doc.set_text_color(*ACCENT)
    doc.multi_cell(0, 6, "Коротко, что внутри:")
    doc.set_x(doc.l_margin + 5)
    doc.set_font("ui", "", 10.5)
    doc.set_text_color(*INK)
    doc.multi_cell(doc.w - doc.l_margin - doc.r_margin - 10, 5.5,
        "1. Что за задача и зачем она в проекте.\n"
        "2. Что мы пробовали - и что честно не сработало.\n"
        "3. Наш ансамбль, который обошёл заявленную SOTA.\n"
        "4. Наша обученная модель и новый метод слияния (fusion).\n"
        "5. Главный результат - \"money plot\": когда текст помогает, а когда нет.")

    h1(doc, "1. О чём вообще речь")
    para(doc, "Представь оператора колл-центра. Ему звонит клиент. Кроме того ЧТО клиент "
              "говорит (слова), важно КАК он это говорит - спокойно, зло, расстроенно, "
              "радостно. Если система понимает эмоцию, она может подсказать оператору: "
              "\"клиент злится - будь мягче, предложи компенсацию\".")
    para(doc, "Распознавание эмоции по голосу называется SER (Speech Emotion Recognition). "
              "Это и есть один из модулей нашего проекта - LLM-ассистента для операторов.")
    callout(doc, "Простыми словами",
            "SER - это модель, которая слушает кусочек речи и говорит: \"тут злость\" "
            "или \"тут грусть\". Мы работаем с русским языком и 4 эмоциями: "
            "нейтрально, злость, радость, грусть.")
    para(doc, "Качество таких моделей измеряют числом macro-F1 (от 0 до 1, больше - "
              "лучше). Это \"средняя точность по всем эмоциям поровну\" - чтобы модель "
              "не жульничала, угадывая только самую частую эмоцию.")

    h1(doc, "2. С чего начали: готовые модели")
    para(doc, "Изобретать с нуля не нужно - есть готовые русские модели, обученные на "
              "датасете Dusha (это большой набор русской речи с размеченными эмоциями, "
              "от Сбера). Мы взяли две сильнейшие:")
    bullets(doc, [
        "HuBERT-Dusha - открытая модель, нейросеть-\"ухо\". macro-F1 = 0.796.",
        "GigaAM-Emo - модель от Сбера, считается лучшей (SOTA) для русского. "
        "macro-F1 = 0.813.",
    ])
    para(doc, "Сначала мы проверили громкое заявление, что GigaAM - SOTA с F1 0.84. "
              "Прогнали сами на одинаковых данных: оказалось 0.813, а \"0.84\" в их "
              "статье измерено на более лёгком подмножестве. Полезный урок: цифры из "
              "чужих README надо перепроверять на своих условиях.")

    h1(doc, "3. Честные тупики (и это нормально)")
    para(doc, "Хорошая исследовательская работа - это не только успехи, но и понимание, "
              "что НЕ работает и почему. Мы попробовали много идей:")
    table(doc,
          ["Что пробовали", "Результат", "Вывод"],
          [["Дообучить голову поверх замороженной модели", "0.643", "хуже - мало ёмкости"],
           ["LoRA-дообучение готовой модели", "0.645", "хуже - \"забывание\""],
           ["Склейка аудио+текст по вероятностям", "0 прироста", "текст не помог"],
           ["Аугментации на инференсе (TTA)", "-3.5 п.п.", "ломает прозодию"],
           ["Третья аудио-модель в ансамбль", "0 прироста", "похожа на вторую"]],
          [0.46, 0.20, 0.34])
    callout(doc, "Почему текст сам по себе не помогал - ключевое открытие",
            "Датасет Dusha сделан так, что СЛОВА намеренно не связаны с эмоцией. "
            "Актёры произносят нейтральные фразы с заданной эмоцией. Пример: фразу "
            "\"Михаил Сергеевич Горбачёв\" актёр произносит ЗЛО - и метка \"злость\", "
            "хотя в словах злости нет. Поэтому текстовая модель на Dusha почти "
            "бесполезна - эмоция живёт только в звуке (это называется "
            "\"паралингвистика\").")

    h1(doc, "4. Первый успех: ансамбль")
    para(doc, "Идея ансамбля: взять две разные модели и усреднить их мнения. Если они "
              "ошибаются в разных местах, вместе они точнее каждой по отдельности - "
              "как консилиум врачей.")
    para(doc, "Мы обучили простой \"комбайнер\" (логистическую регрессию), который "
              "взвешивает ответы GigaAM и HuBERT. Результат:")
    table(doc,
          ["Модель", "macro-F1"],
          [["HuBERT (одна)", "0.796"],
           ["GigaAM (одна, SOTA)", "0.813"],
           ["Наш ансамбль (GigaAM + HuBERT)", "0.855"]],
          [0.62, 0.38])
    callout(doc, "Что это значит",
            "0.855 - это на 4.2 пункта выше заявленной SOTA. Мы НЕ обучали тяжёлых "
            "моделей, просто грамотно соединили две открытые. Это наш первый сильный "
            "результат по качеству.")

    h1(doc, "5. Своя обученная модель")
    para(doc, "Курсовая по машинному обучению должна содержать собственное обучение, а "
              "не только сборку чужого. Прошлые попытки обучения провалились (см. раздел "
              "3), поэтому мы нашли в научных статьях правильный рецепт - "
              "\"partial fine-tuning\":")
    bullets(doc, [
        "взять русский аудио-энкодер wav2vec2 (он умел распознавать речь, но не эмоции);",
        "заморозить нижние слои (они отвечают за базовый звук - их трогать опасно);",
        "дообучить только верхние слои + классификатор эмоций;",
        "обучать аккуратно: малый learning rate, fp16, 4 эпохи на 25 тыс. примеров.",
    ])
    para(doc, "Результат нашей модели на честном тесте (без утечки данных):")
    table(doc,
          ["Модель", "macro-F1", "Значимость"],
          [["HuBERT", "0.796", "-"],
           ["GigaAM (SOTA)", "0.813", "-"],
           ["Наша обученная модель", "0.819", "паритет с SOTA"]],
          [0.45, 0.22, 0.33])
    callout(doc, "Важная честность про статистику",
            "0.819 против 0.813 - это НЕ \"мы обошли Сбер\". Разница в пределах "
            "погрешности (проверили строгим bootstrap-тестом: доверительный интервал "
            "включает ноль). Корректно говорить: наша модель ВЫШЛА НА УРОВЕНЬ SOTA и "
            "значимо обогнала публичный HuBERT (+2.3 п.п., это уже значимо). "
            "Заявлять больше - значит обманывать.")

    h1(doc, "6. Главная идея: обучаемое слияние (fusion)")
    para(doc, "Тут начинается наш собственный научный вклад. Прошлый ансамбль соединял "
              "только финальные ВЕРОЯТНОСТИ (4 числа на модель). Это узкое горлышко - "
              "почти вся информация теряется.")
    para(doc, "Вместо этого мы берём ЭМБЕДДИНГИ - внутренние векторы моделей (256-768 "
              "чисел), в которых на порядок больше информации - и обучаем поверх них "
              "умную сеть слияния с механизмом \"гейтинга\" (GMU).")
    callout(doc, "Что такое гейтинг простыми словами",
            "Гейт - это обучаемый \"кран\". Для каждого звонка сеть сама решает, "
            "сколько доверять каждому источнику: голосу GigaAM, голосу HuBERT, тексту. "
            "Если текст бесполезен (как на Dusha) - кран текста закрывается. Если "
            "текст полезен - открывается. Этому сеть учится сама.")
    para(doc, "Плюс мы добавили \"modality dropout\" - во время обучения случайно "
              "отключаем сильную модальность, чтобы сеть не ленилась и училась "
              "использовать слабую (текст). Это приём из свежих статей против "
              "\"доминирования\" одной модальности.")
    para(doc, "Текстовый канал: мы транскрибируем речь своим ASR (faster-whisper) и "
              "прогоняем через CEDR - русскую модель эмоций по тексту. Именно у неё "
              "лучшая \"дополняющая\" способность из всех, что мы проверили.")

    h1(doc, "7. Главный результат: \"money plot\"")
    para(doc, "Тут была развилка. На Dusha текст не несёт эмоцию (раздел 3), значит и "
              "слияние с текстом прироста не даст - и не дало. Но это вопрос ДАННЫХ, а "
              "не нашего метода. Как доказать, что метод рабочий?")
    para(doc, "Мы построили честный эксперимент с тремя разными наборами данных и "
              "прогнали ОДИН И ТОТ ЖЕ метод слияния на каждом:")
    table(doc,
          ["Данные", "Только звук", "Звук + ТЕКСТ", "Что дал текст"],
          [["Dusha (текст не несёт эмоцию)", "0.849", "0.849", "0 (ничего)"],
           ["RESD (тоже актёрский)", "0.703", "0.658", "-4.5 п.п. (вредит)"],
           ["Наша синтетика (текст несёт эмоцию)", "0.873", "0.944", "+7.1 п.п."]],
          [0.40, 0.18, 0.18, 0.24])
    callout(doc, "В чём соль (это и есть money plot)",
            "Один и тот же метод: на данных, где слова НЕ про эмоцию - гейт закрывает "
            "текст, прироста нет (честный отрицательный результат). На данных, где "
            "слова ЯВНО про эмоцию - тот же гейт открывает текст и даёт +7 пунктов "
            "(значимо, проверено bootstrap). Значит метод РАБОТАЕТ, а отсутствие "
            "прироста на Dusha - это свойство данных, а не нашей ошибки.")
    para(doc, "Синтетику мы сделали так: сгенерировали русские реплики, где эмоция "
              "видна в словах (\"Верните мои деньги, это возмутительно\" - злость; "
              "\"Спасибо огромное, вы мне очень помогли\" - радость), озвучили их "
              "русским синтезатором речи Silero, добавили телефонный шум и сжатие "
              "(как в реальном колл-центре), и даже транскрибировали обратно своим ASR "
              "с ошибками - чтобы всё было честно и реалистично.")
    callout(doc, "Честные оговорки (без них нельзя)",
            "Синтетика - это контролируемый \"зонд\", а не реальные звонки. Silero "
            "передаёт эмоцию голосом ограниченно. Тестовая выборка небольшая (200). "
            "Но эффект крупный и статистически значимый, а главная ценность - именно "
            "КОНТРАСТ трёх наборов на одной архитектуре.")

    h1(doc, "8. Что в итоге собрано")
    bullets(doc, [
        "Проверили заявленную SOTA (GigaAM) сами - 0.813, а не 0.84.",
        "Ансамбль GigaAM+HuBERT = 0.855 - выше SOTA на 4.2 п.п. (без обучения).",
        "Своя обученная модель (partial fine-tuning) = 0.819 - паритет с SOTA, "
        "значимо выше публичного HuBERT.",
        "Новый метод: обучаемое слияние эмбеддингов с гейтингом (GMU + modality dropout).",
        "Money plot: доказали, КОГДА мультимодальность помогает (текст несёт эмоцию) "
        "и когда нет (паралингвистика) - на трёх наборах одной архитектурой.",
        "Везде строгая методика: bootstrap доверительные интервалы, проверка утечек, "
        "абляции, честные отрицательные результаты.",
    ])
    callout(doc, "Научный вклад в одну фразу",
            "Мы не просто гнались за десятыми долями метрики, а ОБЪЯСНИЛИ для русского "
            "SER, когда и почему мультимодальность (текст + голос) помогает - с "
            "обучаемым методом слияния, честными отрицательными результатами на "
            "реальных данных и позитивной демонстрацией механизма на контролируемых.")

    doc.output(str(OUT))
    print(f"saved -> {OUT}")


if __name__ == "__main__":
    build()
