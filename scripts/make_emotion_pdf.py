"""Генерирует docs/emotion.pdf — как именно в проекте распознаётся эмоция (SER)."""

from __future__ import annotations

import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PROJECT_ROOT / "scripts"))

from make_explainer_pdf import (  # noqa: E402
    ACCENT,
    BOX_BG,
    BOX_BORDER,
    INK,
    MUTED,
    Doc,
    bullets,
    callout,
    code,
    h1,
    h2,
    para,
    table,
)

OUT = PROJECT_ROOT / "docs" / "emotion.pdf"


def build() -> None:
    doc = Doc()

    doc.add_page()
    doc.ln(38)
    doc.set_font("ui", "B", 23)
    doc.set_text_color(*ACCENT)
    doc.multi_cell(0, 11, "Распознавание эмоций (SER)")
    doc.ln(2)
    doc.set_font("ui", "", 14)
    doc.set_text_color(*INK)
    doc.multi_cell(0, 7, "Как именно в проекте определяется эмоция клиента\nи как она влияет на подсказки оператору")
    doc.ln(8)
    doc.set_font("ui", "", 11)
    doc.set_text_color(*MUTED)
    doc.multi_cell(0, 6,
        "LLM-копилот для телеком-техподдержки · технический разбор модуля SER\n"
        "(Speech Emotion Recognition) · НИУ ВШЭ · 2026")
    doc.ln(12)
    doc.set_draw_color(*BOX_BORDER)
    doc.set_fill_color(*BOX_BG)
    y = doc.get_y()
    doc.rect(doc.l_margin, y, doc.w - doc.l_margin - doc.r_margin, 26, style="DF")
    doc.set_xy(doc.l_margin + 5, y + 4)
    doc.set_font("ui", "", 10.5)
    doc.set_text_color(*INK)
    doc.multi_cell(doc.w - doc.l_margin - doc.r_margin - 10, 5.5,
        "Короткий ответ: ядро — настоящая нейросеть (HuBERT, ~300 млн параметров), "
        "которая слушает голос и выдаёт одну из 5 эмоций. Шкалы возбуждения/позитива "
        "и флаг «риск эскалации» — простые производные правила поверх ML-выхода.")

    h1(doc, "1. Что мы распознаём")
    para(doc, "SER (Speech Emotion Recognition) — распознавание эмоции по тому, КАК "
              "сказана реплика (тон, темп, интонация), а не по смыслу слов. На выходе — "
              "одна из 5 категорий (таксономия датасета Dusha):")
    bullets(doc, [
        "neutral — нейтрально, спокойно;",
        "angry — раздражение, злость;",
        "positive — позитив, удовлетворённость;",
        "sad — расстроенность, грусть;",
        "other — всё остальное / неоднозначное.",
    ])
    callout(doc, "Почему именно по голосу, а не по тексту",
            "В реальных звонках эмоция чаще в голосе. «Спасибо, очень помогли», сказанное "
            "с сарказмом, по тексту — позитив, а по голосу — раздражение. Поэтому сигнал "
            "берётся из звука. (При этом текст тоже несёт эмоцию — см. раздел 6 про абляцию.)")

    h1(doc, "2. Это ML — какая именно модель")
    para(doc, "Используется готовая нейросеть с HuggingFace:")
    code(doc, "xbgoose/hubert-large-speech-emotion-\n"
              "recognition-russian-dusha-finetuned")
    bullets(doc, [
        "Архитектура: HuBERT-large — transformer-нейросеть (~300 млн параметров), "
        "«родственник» BERT, но обученный слушать звук, а не читать текст.",
        "Сначала предобучена self-supervised на большом объёме речи (научилась "
        "представлять, как устроена устная речь).",
        "Затем дообучена (fine-tuned) на датасете Dusha классифицировать 5 эмоций.",
        "Запускается на видеокарте (GPU), ~50-200 мс на одну реплику.",
        "У нас используется «как есть» (frozen) — мы её не переобучали.",
    ])
    callout(doc, "Это НЕ правила вида «громко = злость»",
            "Сеть сама выучила акустические признаки эмоций (дрожь в голосе, темп, резкость, "
            "интонационный рисунок) на тысячах размеченных людьми примеров. Никаких "
            "вручную прописанных порогов громкости — решение принимает обученная модель.")

    h2(doc, "Что такое датасет Dusha")
    para(doc, "Dusha — крупнейший открытый русский корпус эмоциональной речи: ~350 часов, "
              "300 000+ аудиозаписей с разметкой эмоций живыми аннотаторами. На нём модель "
              "и училась «слышать» эмоции именно в русской речи. По карточке модели её "
              "точность на тестовой части Dusha — F1-macro около 0.81.")

    h1(doc, "3. Как происходит выбор эмоции (по шагам)")
    para(doc, "Аудио проходит через сеть и превращается в вероятности по 5 классам. "
              "Выбираем класс с наибольшей вероятностью:")
    code(doc,
         "аудио 16 кГц, моно\n"
         "   |  feature extractor (нормализация волны)\n"
         "   v\n"
         "HuBERT  ->  logits (5 чисел)\n"
         "   |  softmax (превращает в вероятности, сумма = 1)\n"
         "   v\n"
         "   neutral   0.04\n"
         "   angry     0.72   <- максимум => выбранная эмоция\n"
         "   positive  0.03\n"
         "   sad       0.06\n"
         "   other     0.15\n"
         "   |\n"
         "label = \"angry\",  confidence = 0.72")
    para(doc, "Берём argmax (наибольшую вероятность) — это и есть распознанная эмоция, "
              "а сама вероятность становится «уверенностью» (confidence). На этом ML-часть "
              "заканчивается.")
    callout(doc, "Технические детали",
            "Вход — моно 16 кГц. Длинные реплики обрезаются до 10 секунд (модель училась на "
            "коротких сегментах). Если запись короче ~0.3 с — пропускаем. Один и тот же "
            "звуковой фрагмент идёт и в распознавание речи, и в распознавание эмоции.")

    h1(doc, "4. Что здесь ML, а что — простые правила (честно)")
    para(doc, "Важно различать: сама эмоция — это нейросеть, а две дополнительные величины "
              "достроены поверх неё формулами, а не обучены.")
    table(doc,
          ["Величина", "ML?", "Как получается"],
          [["Эмоция (5 классов)", "ДА", "HuBERT на GPU, argmax вероятностей"],
           ["confidence (уверенность)", "ДА", "максимальная вероятность из softmax"],
           ["arousal / valence\n(возбуждение / позитив)", "НЕТ",
            "справочная таблица по классу: angry -> (0.85, -0.7), sad -> (0.30, -0.55) и т.д."],
           ["escalation_risk\n(риск эскалации)", "НЕТ",
            "правило: класс = angry И confidence > 0.55"]],
          [0.30, 0.12, 0.58])
    callout(doc, "Зачем тогда arousal/valence, если они не из модели",
            "Это две непрерывные шкалы (насколько «на взводе» и насколько негативно), "
            "удобные для интерфейса и промпта. Модель их не выдаёт, поэтому мы приближаем "
            "их таблицей по классу. Для злости с высокой уверенностью arousal дополнительно "
            "поднимается. Это упрощение — в работе оно честно отмечено как ограничение; "
            "полноценная регрессия V/A — задача на будущее.")

    h1(doc, "5. Куда эмоция идёт дальше")
    para(doc, "Распознанная эмоция влияет на пайплайн в одном месте — в задании для LLM, "
              "которая пишет подсказку. В поиск по базе знаний (RAG) эмоция НЕ передаётся.")
    table(doc,
          ["Этап", "Эмоция влияет?", "Что это значит"],
          [["Поиск в базе знаний (RAG)", "НЕТ",
            "какие инструкции находятся — зависит только от текста реплики"],
           ["Генерация ответа (LLM)", "ДА",
            "тон и формулировка подсказки меняются под эмоцию"]],
          [0.33, 0.17, 0.50])
    para(doc, "В промпт для LLM добавляется блок примерно такого вида:")
    code(doc, "## Эмоция клиента\n"
              "angry (уверенность 80%, arousal 0.85, valence -0.70).\n"
              "КЛИЕНТ РАЗДРАЖЁН - сначала признай проблему и извинись,\n"
              "потом конкретные действия.\n"
              "!!! РИСК ЭСКАЛАЦИИ - предложи компенсацию.")
    para(doc, "Из-за этого при злом клиенте подсказка начинается с извинения и предлагает "
              "компенсацию, а при спокойном — сразу переходит к делу.")

    h1(doc, "6. Проверили: влияет ли эмоция на ответ (абляция)")
    para(doc, "Чтобы не просто заявить «эмоция помогает», а измерить — прогнали один и тот "
              "же набор реплик дважды: с блоком эмоции в промпте и без него (всё остальное "
              "идентично). Считали долю ответов с маркерами эмпатии (извинение/понимание) "
              "и с конкретным действием. 15 реплик: 6 злых, 4 грустных, 5 нейтральных.")
    table(doc,
          ["Группа реплик", "Эмпатия ON", "Эмпатия OFF", "Действие ON", "Действие OFF"],
          [["негатив (n=10)", "100%", "90%", "90%", "80%"],
           ["нейтрально (n=5)", "80%", "60%", "80%", "60%"],
           ["все (n=15)", "93%", "80%", "87%", "73%"]],
          [0.32, 0.17, 0.17, 0.17, 0.17])
    callout(doc, "Вывод (честный)",
            "Эмоция в промпте даёт измеримый, но умеренный прирост: +13 п.п. эмпатии и "
            "+14 п.п. конкретики в среднем, ответы чуть длиннее. Прирост небольшой, потому "
            "что базовая линия уже высокая — сам текст несёт настроение («я в бешенстве» "
            "понятно и без сигнала), плюс системный промпт уже просит быть вежливым. "
            "Это согласуется с известным наблюдением (CEMO): эмоция в тексте и в голосе "
            "сильно пересекаются.")
    para(doc, "Ценность этого эксперимента в том, что он воспроизводим и его нельзя "
              "повторить на закрытых конкурентах (Yandex Neurosupport и др.) — там нельзя "
              "«выключить эмоцию» и сравнить.")

    h1(doc, "7. Ограничения честно")
    bullets(doc, [
        "arousal/valence — не предсказание модели, а таблица по классу (не настоящая "
        "регрессия эмоционального пространства).",
        "На синтетической озвучке (TTS) точность эмоции низкая: синтезатор говорит ровным "
        "голосом без настоящего аффекта, и модель видит «нейтрально». На реальных "
        "эмоциональных записях (Dusha) та же модель даёт F1 около 0.81.",
        "Модель используется frozen (без дообучения под телеком) — берём общую "
        "эмоциональную модель как есть.",
        "Эмоция считается отдельно на каждой реплике, без сглаживания по ходу диалога.",
        "Эмоция влияет только на текст подсказки, но не на выбор документов из базы — это "
        "возможная точка роста (учитывать эмоцию и в поиске).",
    ])

    h1(doc, "8. Коротко — одним абзацем")
    para(doc, "Эмоция клиента определяется обученной нейросетью HuBERT, дообученной на "
              "русском эмоциональном корпусе Dusha: она слушает голос и выдаёт вероятности "
              "по 5 эмоциям, мы берём самую вероятную. Поверх неё две вспомогательные "
              "величины (шкалы возбуждения/позитива и флаг эскалации) считаются простыми "
              "правилами. Распознанная эмоция вставляется в задание для LLM и меняет тон "
              "подсказки оператору; на поиск по базе знаний она не влияет. Замер показал, "
              "что эмоция повышает эмпатичность ответов измеримо, хоть и умеренно.")

    OUT.parent.mkdir(parents=True, exist_ok=True)
    doc.output(str(OUT))
    print(f"Saved -> {OUT}  ({OUT.stat().st_size // 1024} KB)")


if __name__ == "__main__":
    build()
