"""Генерирует docs/explainer.pdf — разбор курсовой простым языком, с нуля."""

from __future__ import annotations

from pathlib import Path

from fpdf import FPDF

PROJECT_ROOT = Path(__file__).resolve().parents[1]
OUT = PROJECT_ROOT / "docs" / "explainer.pdf"
FONTS = Path("C:/Windows/Fonts")

INK = (33, 37, 41)
MUTED = (110, 118, 129)
ACCENT = (10, 110, 180)
BOX_BG = (238, 244, 250)
BOX_BORDER = (180, 205, 230)
CODE_BG = (245, 246, 248)
RULE = (210, 215, 222)


class Doc(FPDF):
    def __init__(self) -> None:
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_auto_page_break(auto=True, margin=18)
        self.set_margins(20, 18, 20)
        self.add_font("ui", "", str(FONTS / "arial.ttf"))
        self.add_font("ui", "B", str(FONTS / "arialbd.ttf"))
        self.add_font("ui", "I", str(FONTS / "ariali.ttf"))
        self.add_font("mono", "", str(FONTS / "cour.ttf"))
        self.add_font("mono", "B", str(FONTS / "courbd.ttf"))
        self._toc_target = 0

    def footer(self) -> None:
        if self.page_no() == 1:
            return
        self.set_y(-15)
        self.set_font("ui", "", 8)
        self.set_text_color(*MUTED)
        self.cell(0, 8, f"LLM-копилот для телеком-техподдержки · стр. {self.page_no()}",
                  align="C")


def h1(doc: Doc, text: str) -> None:
    if doc.get_y() > 200:
        doc.add_page()
    doc.ln(3)
    doc.set_font("ui", "B", 17)
    doc.set_text_color(*ACCENT)
    doc.multi_cell(0, 8.5, text)
    doc.set_draw_color(*ACCENT)
    doc.set_line_width(0.5)
    y = doc.get_y() + 1
    doc.line(doc.l_margin, y, doc.w - doc.r_margin, y)
    doc.ln(4)


def h2(doc: Doc, text: str) -> None:
    if doc.get_y() > 245:
        doc.add_page()
    doc.ln(2)
    doc.set_font("ui", "B", 13)
    doc.set_text_color(*INK)
    doc.multi_cell(0, 7, text)
    doc.ln(1.5)


def para(doc: Doc, text: str) -> None:
    doc.set_font("ui", "", 11)
    doc.set_text_color(*INK)
    doc.multi_cell(0, 6, text)
    doc.ln(2)


def bullets(doc: Doc, items: list[str]) -> None:
    doc.set_text_color(*INK)
    indent = 6
    bullet_w = 5
    inner = doc.w - doc.l_margin - doc.r_margin - indent - bullet_w
    for it in items:
        y = doc.get_y()
        if y > doc.h - 25:
            doc.add_page()
            y = doc.get_y()
        doc.set_xy(doc.l_margin + indent, y)
        doc.set_font("ui", "B", 11)
        doc.cell(bullet_w, 6, chr(0x2022))
        doc.set_font("ui", "", 11)
        doc.set_xy(doc.l_margin + indent + bullet_w, y)
        doc.multi_cell(inner, 6, it)
    doc.ln(2)


def callout(doc: Doc, title: str, text: str) -> None:
    doc.ln(1)
    pad = 4
    left = doc.l_margin
    width = doc.w - doc.l_margin - doc.r_margin
    inner = width - 2 * pad

    doc.set_font("ui", "B", 10.5)
    title_lines = doc.multi_cell(inner, 5.5, title, dry_run=True, output="LINES")
    doc.set_font("ui", "", 10.5)
    body_lines = doc.multi_cell(inner, 5.5, text, dry_run=True, output="LINES")
    h = pad * 2 + 5.5 * (len(title_lines) + len(body_lines))

    if doc.get_y() + h > doc.h - 20:
        doc.add_page()

    y0 = doc.get_y()
    doc.set_fill_color(*BOX_BG)
    doc.set_draw_color(*BOX_BORDER)
    doc.set_line_width(0.3)
    doc.rect(left, y0, width, h, style="DF")

    doc.set_xy(left + pad, y0 + pad)
    doc.set_text_color(*ACCENT)
    doc.set_font("ui", "B", 10.5)
    doc.multi_cell(inner, 5.5, title)
    doc.set_x(left + pad)
    doc.set_text_color(*INK)
    doc.set_font("ui", "", 10.5)
    doc.multi_cell(inner, 5.5, text)
    doc.set_y(y0 + h)
    doc.ln(4)


def code(doc: Doc, text: str) -> None:
    doc.ln(1)
    doc.set_font("mono", "", 9.5)
    lines = text.split("\n")
    h = 5 * len(lines) + 4
    left = doc.l_margin
    width = doc.w - doc.l_margin - doc.r_margin
    y0 = doc.get_y()
    doc.set_fill_color(*CODE_BG)
    doc.set_draw_color(*RULE)
    doc.rect(left, y0, width, h, style="DF")
    doc.set_xy(left + 3, y0 + 2)
    doc.set_text_color(*INK)
    for ln in lines:
        doc.set_x(left + 3)
        doc.cell(0, 5, ln)
        doc.ln(5)
    doc.set_y(y0 + h)
    doc.ln(3)


def table(doc: Doc, headers: list[str], rows: list[list[str]], widths: list[float]) -> None:
    doc.ln(1)
    total = doc.w - doc.l_margin - doc.r_margin
    w = [total * x for x in widths]
    doc.set_font("ui", "B", 9.5)
    doc.set_fill_color(*ACCENT)
    doc.set_text_color(255, 255, 255)
    for i, hh in enumerate(headers):
        doc.cell(w[i], 7, " " + hh, border=0, fill=True)
    doc.ln(7)
    doc.set_text_color(*INK)
    fill = False
    for row in rows:
        doc.set_font("ui", "", 9.5)
        line_counts = []
        for i, cell in enumerate(row):
            line_counts.append(max(1, doc.get_string_width(cell) / (w[i] - 3) + 0.99))
        rh = 5.5 * int(max(line_counts) + 0.5)
        rh = max(6.5, min(rh, 22))
        y0 = doc.get_y()
        x0 = doc.l_margin
        if y0 + rh > doc.h - 20:
            doc.add_page()
            y0 = doc.get_y()
        doc.set_fill_color(245, 247, 250) if fill else doc.set_fill_color(255, 255, 255)
        for i, cell in enumerate(row):
            doc.rect(x0, y0, w[i], rh, style="F")
            doc.set_xy(x0 + 1.5, y0 + 1)
            doc.multi_cell(w[i] - 3, 5, cell, align="L")
            x0 += w[i]
            doc.set_xy(x0, y0)
        doc.set_draw_color(*RULE)
        doc.line(doc.l_margin, y0 + rh, doc.w - doc.r_margin, y0 + rh)
        doc.set_y(y0 + rh)
        fill = not fill
    doc.ln(3)


