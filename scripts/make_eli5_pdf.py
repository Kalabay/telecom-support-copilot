"""Генерирует docs/eli5.pdf — объяснение «на пальцах», максимально просто."""

from __future__ import annotations

import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PROJECT_ROOT / "scripts"))

from make_explainer_pdf import (  # noqa: E402
    ACCENT,
    BOX_BG,
    BOX_BORDER,
    INK,
    MUTED,
    Doc,
    bullets,
    callout,
    h1,
    para,
)

OUT = PROJECT_ROOT / "docs" / "eli5.pdf"


def build() -> None:
    doc = Doc()

    doc.add_page()
    doc.ln(40)
    doc.set_font("ui", "B", 24)
    doc.set_text_color(*ACCENT)
    doc.multi_cell(0, 12, "Эмоции по голосу:\nобъясняю на пальцах")
    doc.ln(3)
    doc.set_font("ui", "", 14)
    doc.set_text_color(*INK)
    doc.multi_cell(0, 7, "Что мы делали в курсовой - без единого\nсложного слова. Просто и по порядку.")
    doc.ln(10)
    doc.set_font("ui", "", 11)
    doc.set_text_color(*MUTED)
    doc.multi_cell(0, 6, "Если что-то непонятно - значит я плохо объяснил, а не ты тупой.")

    h1(doc, "Задача: понять, злится ли клиент")
    para(doc, "Звонит человек в техподдержку. Важно не только ЧТО он сказал, но и КАК - "
              "спокойно, зло, грустно или радостно. Если компьютер это поймёт, он "
              "подскажет оператору: \"клиент на нервах, будь помягче\".")
    callout(doc, "Аналогия",
            "Это как умный помощник, который слушает звонок вместе с оператором и "
            "шепчет ему на ухо: \"осторожно, человек злится\".")

    h1(doc, "Шаг 1: взяли готовые \"уши\"")
    para(doc, "Никто не делает такое с нуля. Есть готовые нейросети, которые уже умеют "
              "слушать русскую речь и угадывать эмоцию. Мы взяли две лучшие - одна "
              "обычная, вторая от Сбера (считается самой крутой).")
    para(doc, "Сначала проверили: правда ли сберовская такая крутая, как хвалят? "
              "Прогнали сами - оказалось, цифры в рекламе были чуть приукрашены. "
              "Урок: чужим обещаниям не верь, проверяй сам.")

    h1(doc, "Шаг 2: много чего НЕ сработало")
    para(doc, "Мы перепробовали кучу идей, и многие провалились. Это нормально - "
              "наука так и работает: пробуешь, не вышло, понимаешь почему.")
    callout(doc, "Самое важное, что мы поняли",
            "В наших данных СЛОВА специально не связаны с эмоцией. Актёры говорили "
            "нейтральные фразы, но с разным настроением. Например, фразу \"передай "
            "соль\" можно сказать и зло, и ласково - слова одни, эмоция разная. "
            "Поэтому понять эмоцию ТОЛЬКО по тексту тут невозможно - надо слушать голос.")

    h1(doc, "Шаг 3: соединили две модели - стало лучше")
    para(doc, "Идея простая, как консилиум врачей: если спросить двух разных "
              "специалистов и совместить их мнения, ответ будет точнее, чем у каждого "
              "по отдельности.")
    callout(doc, "Результат",
            "Объединив две модели, мы получили точность ВЫШЕ, чем у хвалёной "
            "сберовской - и при этом сами ничего тяжёлого не обучали, просто умно "
            "соединили готовое.")

    h1(doc, "Шаг 4: обучили свою модель")
    para(doc, "Курсовая по машинному обучению должна содержать СВОЁ обучение, а не "
              "только сборку чужого. Прошлые попытки обучать проваливались, пока мы не "
              "нашли в научных статьях правильный способ.")
    para(doc, "Смысл способа: у нейросети есть \"нижние\" слои (распознают базовые "
              "звуки) и \"верхние\" (распознают смысл). Нижние трогать опасно - "
              "заморозили. Дообучили только верхние под эмоции. Сработало!")
    callout(doc, "Честно про результат",
            "Наша модель догнала сберовскую (вышла на тот же уровень), но НЕ обогнала "
            "её - разница в пределах случайной погрешности. Говорить \"мы победили "
            "Сбер\" было бы враньём. А вот обычную открытую модель мы обогнали "
            "по-настоящему.")

    h1(doc, "Шаг 5: наш главный фокус - умное соединение")
    para(doc, "Это самая интересная часть. Мы сделали \"умный кран\", который для "
              "каждого звонка сам решает, кого слушать: голос или слова.")
    callout(doc, "Аналогия с краном",
            "Представь три крана: голос-1, голос-2 и текст. Наша сеть сама "
            "подкручивает их. Если в словах эмоции нет (как в наших данных) - кран "
            "текста закрывается. Если слова явно эмоциональные - кран текста "
            "открывается. Сеть научилась этому сама.")

    h1(doc, "Главный вывод: когда текст помогает")
    para(doc, "Мы доказали простую, но важную вещь. Взяли ОДИН И ТОТ ЖЕ наш метод и "
              "проверили на трёх типах данных:")
    bullets(doc, [
        "Данные, где слова НЕ про эмоцию (как у нас) -> текст не помогает, и кран его "
        "закрывает. Это честный \"отрицательный\" результат.",
        "Ещё одни такие же данные -> то же самое, текст даже мешает.",
        "Данные, где слова ЯВНО про эмоцию (\"верните деньги, это возмутительно!\") -> "
        "тот же метод ОТКРЫВАЕТ текст и становится заметно точнее.",
    ])
    callout(doc, "Зачем это нужно (это и есть наша главная мысль)",
            "Мы не просто гнались за красивой цифрой. Мы ОБЪЯСНИЛИ, когда соединять "
            "голос и текст полезно, а когда бессмысленно. И доказали, что наш метод "
            "сам понимает разницу. Это важнее, чем просто \"наша цифра больше\".")

    h1(doc, "Итог одним абзацем")
    para(doc, "Мы взяли лучшие готовые модели распознавания эмоций по русскому голосу, "
              "проверили их честность, соединили в более точную систему, обучили свою "
              "собственную модель и придумали умный способ совмещать голос с текстом. "
              "А главное - разобрались, КОГДА текст реально помогает понять эмоцию, а "
              "когда надо слушать только голос. Всё это собрано в рабочую систему для "
              "оператора колл-центра.")
    doc.ln(4)
    doc.set_font("ui", "I", 11)
    doc.set_text_color(*MUTED)
    doc.multi_cell(0, 6, "Более подробный разбор с цифрами и таблицами - в файле "
                          "fusion_story.pdf.")

    doc.output(str(OUT))
    print(f"saved -> {OUT}")


if __name__ == "__main__":
    build()
